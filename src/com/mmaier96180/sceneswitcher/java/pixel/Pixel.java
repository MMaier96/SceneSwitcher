package com.mmaier96180.sceneswitcher.java.pixel;

import java.awt.Color;

public class Pixel {
	private double x;
	private double y;
	private Color color;
	
	public Pixel() {
	}

	public double getX() {
		return x;
	}

	public void setX(double x) {
		this.x = x;
	}

	public double getY() {
		return y;
	}

	public void setY(double y) {
		this.y = y;
	}

	public Color getColor() {
		return color;
	}

	public void setColor(Color color) {
		this.color = color;
	}
	
}
